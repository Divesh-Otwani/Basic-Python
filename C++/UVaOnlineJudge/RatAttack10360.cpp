
#include <iostream>
using namespace std;

/* Design idea:
 *      just make a set of all points in range of some location
 *      then filter through them
 *
 *
 *
 *
 */







int main(){
    long int sum = 0;
    for(int i = 0; i < 1000000000; ++i){
        sum += i;
    }
    cout << sum << endl;
}

